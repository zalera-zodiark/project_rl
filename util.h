
int cube(int n) {
    return n * n * n;
}

int square(int n) {
    return n * n;
}

int addOne(int n) {
    return n++;
}

int restOne(int n) {
    return n--;
}

int sum(int n1, int n2) {
    return n1 + n2;
}

int rest(int n1, int n2) {
    return n1 - n2;
}

int multiplication(int n1, int n2) {
    return n1 * n2;
}

int division(int n1, int n2) {
    return n1 / n2;
}

int mod(int n1, int n2) {
    return n1 % n2;
}